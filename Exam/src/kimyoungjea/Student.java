package kimyoungjea;

public class Student {
	String name;
	int nYear;
	int nClass;
	int nNumber;
	String birthday;
	String gender;
	String mobile;
	@Override
	public String toString() {
		return "Student [name=" + name + ", nYear=" + nYear + ", nClass=" + nClass + ", nNumber=" + nNumber
				+ ", birthday=" + birthday + ", gender=" + gender + ", mobile=" + mobile + "]";
	}
}
